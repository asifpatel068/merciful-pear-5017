function Sortby(){
    let s=document.querySelector("#Sort").value;
    console.log(1)
    if(s=="Price Low to High"){
       let newData= productsObject.sort((a,b)=>{return a.price-b.price});
       displayItem(newData);
    }
    else if(s=="Price High to Low"){
        let newData=productsObject.sort((a,b)=>{return b.price-a.price})
        displayItem(newData);
    }
    else if(s=="New items"){
        let newData= productsObject.reverse()
        displayItem(newData);
    }
    else{
        displayItem(productsObject);
    }
    
}

function serachmen(){
    let sd=document.querySelector("#searchm").value

    let newarr=productsObject.filter(function (ele){
            return ele.name.toLowerCase().includes(sd.toLowerCase())||ele.brand.toLowerCase().includes(sd.toLowerCase())||ele.category.toLowerCase().includes(sd.toLowerCase())
    })
    
    displayItem(newarr);
}


function displayItem(data){
document.querySelector(".container").textContent=""

    data.map(function (ele){

        if(ele.gender=="Male"){
        let div = document.createElement("div");

        let image = document.createElement("img")
        image.setAttribute("src",ele.image)
     
        let brand = document.createElement("p")
        brand.textContent = ele.brand
        brand.setAttribute("class","nameFont")

        let name = document.createElement("p")
        name.textContent = ele.name
        name.setAttribute("class","nameFont")
    
        let price_button_div = document.createElement("div")
    
        let price = document.createElement("h5")
        price.textContent= "â‚¬"+" "+ ele.price
        price.setAttribute("class","price")
        
        let button = document.createElement("button")
        button.textContent="Add Cart"
        button.addEventListener("click", function(){
            count++;
            localStorage.setItem("Count",count)
            document.querySelector("#quantity").textContent=count;

            cart_Items.push(ele)
            localStorage.setItem("cartdata",JSON.stringify(cart_Items))
            
        })
    
        price_button_div.append(price,button)
        
    
        div.append(image,brand,name,price_button_div)
        document.querySelector(".container").append(div)
    }
    })


}
